  <h1>Events</h1>
  <p>There are a number of events planned for the three days of the Meet. It will be a perfect blend of nostalgia, fun, professionalism and entertainment.</p>

  <p><font size="4px">1.</font>

    <span style="font-size:21px;color:#F00">Inauguration Ceremony: </span>    A formal way to get started and welcome our alumni back to the campus. This auspicious occasion, with the invocation ceremony by the hands of the Director, marks the beginning of the three-day long event. There will be documentaries premiered on KGP to help you recapitulate of what had happened and what is happening in your college.</p>
    <p><img src="events_images/inaguration.jpg"></p>

    <p><font size="4px">2.</font><span style="font-size:21px;color:#F00">Campus Tour: </span>         The whole batch will be taken around the campus with our volunteers and will be updated about the current developments in the campus. You will be also visiting your departments and the old beauties of the campus.</p>
    <img src="events_images/campus_tour2.jpg">



    <p><font size="4px">3.</font><span style="font-size:21px;color:#F00">Hall Visits:  </span>     A night is solely dedicated to your hall- where you can revisit your room and relive the best times of your college life. The halls would be organising events for their respective alumni and after that its all yours....
    </p>
    <img src="events_images/hall_visit.jpg">

    <p><font size="4px">4.</font><span style="font-size:21px;color:#F00">Musical Night:  </span>                                  The mood transcends all senses as you go Gaga over Raga. A musical bonanza, where live performances shall floor you with their intoxicating blend of music and sounds, and perhaps get you closer to musical nirvana. So get ready to sway to some mesmerizing performances by the musicians of Kgp. Previous meets have set a precedent where mesmerised alumni take to the dais.</p>



