					<h2  >Students' Alumni Cell</h2>
					<br>
				</p>

			</center>

			Over the course of past few years, <strong>Students' Alumni Cell</strong> has emerged as a key organ of the

			<strong>Office of Alumni Affairs and International Relations</strong> in executing its yearlong events and

			activities. Students' Alumni Cell continuously endeavours to strengthen the relations of the

			alumni of this Institute and their alma mater. Formed as and operating under the Dean,

			Alumni Affairs and International Relations, Students' Alumni Cell has been instrumental in

			organizing the <strong>Annual Alumni Meet</strong> and <strong>Regional Student-Alumni Meets</strong>, publishing the quarterly alumni newsletter (KGPian), the

			annual literary magazine (Yearnings of Yore) and the <strong>Annual Yearbook (Kognosco Gaudeo

			Perficio)</strong> and organizing regular guest lectures by distinguished alumni via the initiatives

			“Guest Lecture Conclave” and the “E-Guest Lecture Series”. Students' Alumni Cell also

			operates the Student - Alumni Mentorship Programme, My Imprint (Giving Back - Alumni

			contribution) and organises Alvida (the annual farewell dinner).
